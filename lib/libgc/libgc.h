#ifndef LIBGC_H
# define LIBGC_H

// =============================================================================
// Section : Includes
// =============================================================================

# include <stdlib.h>

// =============================================================================
// Section : Type Definitions
// =============================================================================

// ·············································································
// Sub-section : Structure t_gc_node
// ·············································································

typedef struct s_gc_node
{
	void				*content;
	size_t				dimension;
	struct s_gc_node	*next;
	struct s_gc_node	*prev;
}	t_gc_node;

// ·············································································
// Sub-section : Structure t_gc
// ·············································································

typedef struct s_gc
{
	t_gc_node	*ptr;
	void		*last;
	void		*current;
}	t_gc;

// =============================================================================
// Section : Functions
// =============================================================================

t_gc	**gc_get(void);
void	*gc_add(void *ptr, size_t dimension);
void	gc_free(void *ptr);
void	gc_sweep(void);

#endif
